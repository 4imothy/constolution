        self.branch7x7dbl_1 = BasicConv2d(in_channels, c7, kernel_size=1)
        self.branch7x7dbl_2 = BasicPDConv2d(ct.Kernels.Box, c7, c7, kernel_size=(7, 1), padding=(3, 0))
